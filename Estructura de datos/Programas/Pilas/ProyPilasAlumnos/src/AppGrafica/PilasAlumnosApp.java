    private void jButReacomodoTopeFondoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButReacomodoTopeFondoActionPerformed
        // TODO add your handling code here:
        Pila grupo = rb1.isSelected()?grupo1:grupo2;

        
        if(grupo.Length()>3){
        Pila aux = new Pila();
        
        TipoElem tope = grupo.Pop();
        TipoElem segTope = grupo.Pop(); 
        
        while(grupo.Length()>1)
            aux.Push(grupo.Pop());
      
        
        TipoElem fondo = grupo.Pop();
        TipoElem segFondo = grupo.Pop();
        
        grupo.Push(fondo);
        grupo.Push(segTope);
        while(!aux.Empty())
            grupo.Push((aux.Pop()));
        grupo.Push(segFondo); 
        grupo.Push(tope);
         JOptionPane.showMessageDialog(this, "Intercambio del tope con el fondo.",
                                            "Operacion hecha",JOptionPane.PLAIN_MESSAGE);
        }
         else
         JOptionPane.showMessageDialog(this, "No se puede intercambiar del tope con el fondo.",
                                            "Pila no par",JOptionPane.PLAIN_MESSAGE);
    }//GEN-LAST:event_jButReacomodoTopeFondoActionPerformed

    
    private void ElimiFondoTope(Pila grupo){
        if(grupo.Length()>1){
        Pila aux = new Pila();
         grupo.Pop();  
        while(grupo.Length()>1)
            aux.Push(grupo.Pop());   
          grupo.Pop(); 
        while(!aux.Empty())         
            grupo.Push(aux.Pop());   
        
        }
    }
