    private void btnVisuaGrupoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVisuaGrupoActionPerformed
      Pila grupo = rb1.isSelected()? grupo1 : grupo2;
      Pila aux = new Pila(grupo.Length()); 
      lblGrupo.setText(rb1.isSelected()? "GRUPO 1" : "GRUPO 2");
      DefaultTableModel modelo = (DefaultTableModel) tblVisuaGrupo.getModel();
      modelo.setRowCount(0);
      
      while(! grupo.Empty()) {
          TipoElem elem = grupo.Pop();
            aux.Push(elem);
        Object[] o = new Object[4];
            o[0] = elem.getNoCon();
            o[1] = elem.getNombre();
            o[2] = elem.getCalif();
            o[3] = elem.getSexo();
            modelo.addRow(o);
        }
        while(! aux.Empty()) 
            grupo.Push(aux.Pop());
    }//GEN-LAST:event_btnVisuaGrupoActionPerformed
    
    public void Reacomodo(Pila grupo){
    if(grupo.Length()>1){
       Pila auxHom = new Pila();
       Pila auxMuj = new Pila();
       while(!grupo.Empty()){
           TipoElem sexo = grupo.Pop();
           if(sexo.getSexo()=='M')
               auxHom.Push(sexo);
           else 
               auxMuj.Push(sexo);
       }
       while(!auxHom.Empty()){
          grupo.Push(auxHom.Pop());
       }
       while(!auxMuj.Empty()){
          grupo.Push(auxMuj.Pop());
        }
       }
      
    }
    
