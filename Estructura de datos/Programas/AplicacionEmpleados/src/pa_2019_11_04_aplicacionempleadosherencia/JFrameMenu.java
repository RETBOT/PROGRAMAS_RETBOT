    public boolean agregarEmpleado(Empleado e){
    if(empleadosRegistrados == empresa.length)
    return false;
    
    empresa[empleadosRegistrados] = e;
    empleadosRegistrados++;
    jLabRegistrados.setText(empleadosRegistrados+"");
    return true;
    
    }
    
    
