    private void jButIntercambioUPUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButIntercambioUPUActionPerformed
        // TODO add your handling code here:
        ArrayList<Automovil> grupoSel = getGrupoSel();
        if(grupoSel.size()>1){
       
           Automovil cambio1 = grupoSel.get(grupoSel.size()-1);
           Automovil cambio2 = grupoSel.get(grupoSel.size()-2);
           
           grupoSel.set(grupoSel.size()-2, cambio1);
           grupoSel.set(grupoSel.size()-1, cambio2);
        
        JOptionPane.showMessageDialog(this, "Se ha realizado el intercambio.");
        }
        else
            JOptionPane.showMessageDialog(this, "El grupo seleccionado es menor a 2 automoviles");
    }//GEN-LAST:event_jButIntercambioUPUActionPerformed

