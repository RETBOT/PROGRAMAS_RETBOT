     private ArrayList<Automovil> getGrupoSel() {
        int noGrupoSel = jComBoxGrupos.getSelectedIndex();
        switch (noGrupoSel) {
            case 0: 
                return grupo1;
            case 1: 
                return grupo2;
            default:
                return grupo1;
        } 
     }
    
    private void jButLecturaAutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButLecturaAutoActionPerformed
        ArrayList<Automovil> grupoSel = getGrupoSel();
        Automovil auto = new Automovil();
   
        auto.setMarca(jTextMarca.getText());
        auto.setModelo(jTextModelo.getText());
        auto.setPresionLlanta(Integer.parseInt(jTextPresionLlantas.getText()));
        auto.setCosto(Integer.parseInt(jTextCosto.getText()));
        grupoSel.add(auto);
        
        jTableAutomoviles.setValueAt(grupoSel.size(), 0, jComBoxGrupos.getSelectedIndex());
        
    }//GEN-LAST:event_jButLecturaAutoActionPerformed

    private void jButVisuaGpoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButVisuaGpoActionPerformed
        // TODO add your handling code here:
        ArrayList<Automovil> grupoSel = getGrupoSel();
        jLabNombreGrupo.setText("Grupo "+ (jComBoxGrupos.getSelectedIndex()+1));
        DefaultTableModel modelo = (DefaultTableModel)jTableVisuaAuto.getModel();
        modelo.setRowCount(0);
        
        int i = 0;
        for (Automovil automovil : grupoSel) {
            Object[] a = new Object[5];
            a[0] = ++i;
            a[1] = automovil.getMarca();
            a[2] = automovil.getModelo();
            a[3] = automovil.getPresionLlanta();
            a[4] = automovil.getCosto();
            modelo.addRow(a);
        }
    }//GEN-LAST:event_jButVisuaGpoActionPerformed

    private void jButIntercambioRefActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButIntercambioRefActionPerformed
        // TODO add your handling code here:
        ArrayList<Automovil> aux = grupo1;
        grupo1 = grupo2;
        grupo2 = aux;
        
        jTableAutomoviles.setValueAt(grupo1.size(),0,0);
        jTableAutomoviles.setValueAt(grupo2.size(),0,1);
        
        JOptionPane.showMessageDialog(this, "El intercambio de automoviles entre el grupo 1 y 2 se ha realizado");
    }//GEN-LAST:event_jButIntercambioRefActionPerformed

    private void jButIntercambioCopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButIntercambioCopActionPerformed
        ArrayList<Automovil> aux = new ArrayList<>();
        
        for (Automovil automovil : grupo1) 
            aux.add(automovil);
        grupo1.clear(); 
        for (Automovil automovil : grupo2)
           grupo1.add(automovil);
        grupo2.clear();
        for (Automovil automovil : aux)
            grupo2.add(automovil);
    
        jTableAutomoviles.setValueAt(grupo1.size(), 0, 0);
        jTableAutomoviles.setValueAt(grupo2.size(), 0, 1);
        
        JOptionPane.showMessageDialog(this, "El intercambio entre el grupo 1 y 2 se ha realizado");
    }//GEN-LAST:event_jButIntercambioCopActionPerformed

    private void elimiAutos(ArrayList<Automovil>grupo,int col){
    for(int i=0;i<grupo.size();i++)
    if(Integer.parseInt(grupo.get(i).getModelo())>= 2000 && Integer.parseInt(grupo.get(i).getModelo())<= 2014){
    grupo.remove(i);
    --i;
    }
     jTableAutomoviles.setValueAt(grupo.size(), 0, col);
    }
    
