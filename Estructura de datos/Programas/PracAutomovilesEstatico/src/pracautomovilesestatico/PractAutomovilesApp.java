     private Automovil [] getGrupoSel() {
        int noGrupoSel = jComBoxGrupos.getSelectedIndex();
        switch (noGrupoSel) {
            case 0: 
                return grupo1;
            case 1: 
                return grupo2;
            default:
                return grupo1;
        } 
     }
    
    private void jButLecturaAutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButLecturaAutoActionPerformed
        Automovil [] grupoSel = getGrupoSel();
        int noGrupoSel = jComBoxGrupos.getSelectedIndex();
        if(noAutos[noGrupoSel]<MaxAuto){
        grupoSel[noAutos[noGrupoSel]++].Leer(jTextMarca.getText(), 
                                             jTextModelo.getText(), 
                   Integer.parseInt(jTextPresionLlantas.getText()), 
                           Integer.parseInt(jTextCosto.getText()));

        jTableAutomoviles.setValueAt(noAutos[noGrupoSel], 0, noGrupoSel);
       
        }
        else 
            JOptionPane.showMessageDialog(this, "Ya no hay cupo en el grupo seleccionado");  
    }//GEN-LAST:event_jButLecturaAutoActionPerformed

    private void jButVisuaGpoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButVisuaGpoActionPerformed
         Automovil [] grupoSel = getGrupoSel();
        jLabNombreGrupo.setText("Grupo "+ (jComBoxGrupos.getSelectedIndex()+1));
        DefaultTableModel modelo = (DefaultTableModel)jTableVisuaAuto.getModel();
        modelo.setRowCount(0);
        
        int noGrupoSel = jComBoxGrupos.getSelectedIndex();
        for (int i=0;i<noAutos[noGrupoSel];i++) {
            Object[] a = new Object[5];
            a[0] = i+1;
            a[1] = grupoSel[i].getMarca();
            a[2] = grupoSel[i].getModelo();
            a[3] = grupoSel[i].getPresionLlanta();
            a[4] = grupoSel[i].getCosto();
            modelo.addRow(a);
        }
    }//GEN-LAST:event_jButVisuaGpoActionPerformed

    private void jButIntercambioRefActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButIntercambioRefActionPerformed
        Automovil [] aux = grupo1;
        grupo1 = grupo2;
        grupo2 = aux;
         
        int auxNoAutos = noAutos[0];
        noAutos[0] = noAutos[1];
        noAutos[1] = auxNoAutos;
        
        jTableAutomoviles.setValueAt(noAutos[0],0,0);
        jTableAutomoviles.setValueAt(noAutos[1],0,1);
        
        JOptionPane.showMessageDialog(this, "El intercambio de automoviles entre el grupo 1 y 2 se ha realizado");
    }//GEN-LAST:event_jButIntercambioRefActionPerformed

