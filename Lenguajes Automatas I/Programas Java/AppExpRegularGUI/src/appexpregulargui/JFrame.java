    private void jButCoincideActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCoincideActionPerformed
        // TODO add your handling code here:
       jTextAreaResultadoBusqueda.setText("");
        String cadena = jTextCadena.getText();
        Pattern patron = Pattern.compile(jTextPatron.getText());
        Matcher matcher = matcher = patron.matcher(cadena);
        String todo = "1";
        try{
         todo = JOptionPane.showInputDialog("Coincidir con toda la cadena \n1.Si\n2.No");
        }catch(NumberFormatException ex){
            todo = "2";
        }
        int opc = Integer.parseInt(todo);
        if(opc == 1){
        if(matcher.matches())
            jTextAreaResultadoBusqueda.setText("Coincide");
        else 
            jTextAreaResultadoBusqueda.setText("No coincide");
        }else {
            if(matcher.lookingAt())
            jTextAreaResultadoBusqueda.setText("Coincide");
        else 
            jTextAreaResultadoBusqueda.setText("No coincide");
        }
            
    }//GEN-LAST:event_jButCoincideActionPerformed

    private void jButBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButBuscarActionPerformed
        // TODO add your handling code here:
        jTextAreaResultadoBusqueda.replaceSelection(" ");
        jTextAreaResultadoBusqueda.setText("");
         String REGEX = jTextPatron.getText();
         String INPUT = jTextCadena.getText();

        Pattern p = Pattern.compile(REGEX);
        Matcher m = p.matcher(INPUT);   // get a matcher object
        int count = 0;
      while(m.find()) {
         count++;
         jTextAreaResultadoBusqueda.append("coincidencia n√∫mero "+count);
         jTextAreaResultadoBusqueda.append("\ncomienzo: "+m.start());
         jTextAreaResultadoBusqueda.append("\nfin: "+m.end()+"\n");
         
      }
    }//GEN-LAST:event_jButBuscarActionPerformed

    private void jButRemplazarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButRemplazarActionPerformed
        // TODO add your handling code here:
        jTextAreaResultadoBusqueda.setText("");
        String REGEX = jTextPatron.getText();
        String INPUT = jTextCadena.getText();
        String REPLACE = JOptionPane.showInputDialog("Cadena a remplazar en el texto");
        Pattern p = Pattern.compile(REGEX);
        Matcher m = p.matcher(INPUT); 
        INPUT = m.replaceAll(REPLACE);
        jTextAreaResultadoBusqueda.append(INPUT);
    }//GEN-LAST:event_jButRemplazarActionPerformed

