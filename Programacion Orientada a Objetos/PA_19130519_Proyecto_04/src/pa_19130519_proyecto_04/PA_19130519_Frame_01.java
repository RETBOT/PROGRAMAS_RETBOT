    private void jMenuGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuGuardarActionPerformed
        // TODO add your handling code here:

        if(jfch.showSaveDialog(jMenu1)!=0)
        return;

        FileOutputStream fos=null;
        DataOutputStream dos=null;
        try {

            fos = new FileOutputStream(jfch.getSelectedFile());
            dos = new DataOutputStream(fos);

            dos.writeInt(modelo.getRowCount());

            //recorrer el jTable
            for(int r=0;r<modelo.getRowCount();r++){
                dos.writeInt(Integer.parseInt(modelo.getValueAt(r, 0)+""));
                dos.writeInt(Integer.parseInt(modelo.getValueAt(r, 1)+""));
            }
            dos.writeUTF(jTextIdentificador.getText());
            dos.writeUTF(jTextColor.getText());

        } catch (FileNotFoundException ex) {
            Logger.getLogger(PA_19130519_Frame_01.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(PA_19130519_Frame_01.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            try {
                if(dos!=null)
                dos.close();
                if(fos!=null)
                fos.close();
            } catch (IOException ex) {
                Logger.getLogger(PA_19130519_Frame_01.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jMenuGuardarActionPerformed

    private void jMenuAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuAbrirActionPerformed
        // TODO add your handling code here:
        if(jfch.showSaveDialog(jMenu1)!=0)
        return;

        FileInputStream fis = null;
        DataInputStream dis = null;

        try {
            fis = new FileInputStream(jfch.getSelectedFile());
            dis = new DataInputStream(fis);

            int n = dis.readInt();

            jSpinLineas.setValue(n);

            for(int r=0;r<modelo.getRowCount();r++){
                modelo.setValueAt(dis.readInt(), r, 0);
                modelo.setValueAt(dis.readInt(), r, 1);
            }
            jTextIdentificador.setText(dis.readUTF());
            jTextColor.setText(dis.readUTF());

        } catch (FileNotFoundException ex) {
            Logger.getLogger(PA_19130519_Frame_01.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(PA_19130519_Frame_01.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            try {
                if(dis!=null)
                dis.close();
                if(fis!=null)
                fis.close();
            } catch (IOException ex) {
                Logger.getLogger(PA_19130519_Frame_01.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jMenuAbrirActionPerformed

