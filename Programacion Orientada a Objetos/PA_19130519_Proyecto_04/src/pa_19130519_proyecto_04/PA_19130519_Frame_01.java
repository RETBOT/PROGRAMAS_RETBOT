    public void dibujarPoligono(int [] x, int [] y, Component componente){
     
        dibujar = componente.getGraphics();
        dibujar.setColor(componente.getBackground());
        dibujar.fillRect(5, 5, componente.getWidth(), componente.getHeight());
                  
        if(jTextColor.getText().equals("1")){
        dibujar.setColor(Color.black);
        }
        if(jTextColor.getText().equals("2")){
        dibujar.setColor(Color.green);
        }
        if(jTextColor.getText().equals("3")){
        dibujar.setColor(Color.red);
        }
        if(jTextColor.getText().equals("4")){
        dibujar.setColor(Color.blue);
        }
        if(jTextColor.getText().equals("5")){
        dibujar.setColor(Color.gray);
        }
        if(jTextColor.getText().equals("6")){
        dibujar.setColor(Color.orange);
        }
        if(jTextColor.getText().equals("7")){
        dibujar.setColor(Color.yellow);
        }
        if(jTextColor.getText().equals("8")){
        dibujar.setColor(Color.magenta);
        }
        
        dibujar.drawPolygon(x, y, modelo.getRowCount());
        
        int xCenter = componente.getWidth() / 2;
        int yCenter = componente.getHeight() / 2;
        dibujar.setColor(Color.black);
        dibujar.drawString(jTextIdentificador.getText(), xCenter, yCenter);
    }
    
