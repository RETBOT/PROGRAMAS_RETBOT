    private void jListOficinaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListOficinaValueChanged
        // TODO add your handling code here: 
        if(jRadioSillon1.isSelected()){
        jTextOficina.setText("Descripción:"+s.getDescripcion()+"\nPrecio:"+s.getPrecio()+"\nColor:"+s.getColor()+"\nDimensiones"+s.getDimensiones()); 
        
        s.dibujar(1,jPanelOficina);
        }
        
        if(jRadioGabinete1.isSelected()){
        jTextOficina.setText("Descripción:"+g.getDescripcion()+"\nPrecio:"+g.getPrecio()+"\nColor:"+g.getColor()+"\nDimensiones:"+g.getDimensiones());
       
        g.dibujar(1,jPanelOficina);
        }
        
        if(jRadioMonitor1.isSelected()){
        jTextOficina.setText("Descripción:"+m.getDescripcion()+"\nPrecio:"+m.getPrecio()+"\nMarca:"+m.getMarca()+"\nDimensiones:"+m.getDimensiones());
       
        m.dibujar(1,jPanelOficina);
        }
        
        if(jRadioImpresora1.isSelected()){
        jTextOficina.setText("Descripción:"+i.getDescripcion()+"\nPrecio:"+i.getPrecio()+"\nMarca:"+i.getMarca()+"\nDimensiones:"+i.getDimensiones());
       
        i.dibujar(1,jPanelOficina);
        }
        
    }//GEN-LAST:event_jListOficinaValueChanged

