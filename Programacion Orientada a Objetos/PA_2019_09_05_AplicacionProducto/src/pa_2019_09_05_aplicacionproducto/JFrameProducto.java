    private void jButAleatoriosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButAleatoriosActionPerformed
        // TODO add your handling code here:
        Random azar = new Random();
        
        String [] desc = {"Televisión","Estereo","Bocina","Microfono","Radio",
                          "Sala","Sillón","Librero"};
        
        double [] imp = {0,6,10,16,20,35,45.6};
        
        jTextDescripcion.setText(desc[azar.nextInt(desc.length)]);
        jTextCosto.setText((1+azar.nextInt(20))*500+"");
        jTextUtilidad.setText(5+azar.nextInt(26)+"");
        jTextDescuento.setText(azar.nextInt(16)+"");
        jTextImpuesto.setText(imp[azar.nextInt(imp.length)]+"");
        
        
    }//GEN-LAST:event_jButAleatoriosActionPerformed

    private void jButCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcularActionPerformed
        // TODO add your handling code here:
        Producto prod = new Producto(jTextDescripcion.getText(),
         Double.parseDouble(jTextCosto.getText()), 
         Double.parseDouble(jTextUtilidad.getText()),
         Double.parseDouble(jTextDescuento.getText()),
         Double.parseDouble(jTextImpuesto.getText()));
 
        DecimalFormat df = new DecimalFormat("#,###,##0.00");
        jLabUtilidad.setText(df.format(prod.importeUtilidad()));
        jLabSubtot1.setText(df.format(prod.subTotal1()));
        jLabDescuento.setText(df.format(prod.importeDescuento()));
        jLabSubtot2.setText(df.format(prod.subTotal2()));
        jLabImpuesto.setText(df.format(prod.importeImpuesto()));
        jLabPrecio.setText(df.format(prod.precio()));
        
    }//GEN-LAST:event_jButCalcularActionPerformed

