    public void crearA(){
         A = new Matriz(modeloA.getRowCount(),modeloA.getColumnCount());
        for (int r = 0; r < modeloA.getRowCount(); r++) 
            for (int c = 0; c < modeloA.getColumnCount(); c++) 
                A.setValor(Double.parseDouble(modeloA.getValueAt(r, c)+""), r, c);
    }
    public void crearB(){
        B = new Matriz(modeloB.getRowCount(),modeloB.getColumnCount());        
        for (int r = 0; r < modeloB.getRowCount(); r++) 
            for (int c = 0; c < modeloB.getColumnCount(); c++) 
                B.setValor(Double.parseDouble(modeloB.getValueAt(r, c)+""), r, c);
    }
    public void mostrarC(){
        
        modeloC.setRowCount(C.getRenglones());
        modeloC.setColumnCount(C.getColumnas());
        
        jLabRC.setText(C.getRenglones()+"");
        jLabCC.setText(C.getColumnas()+"");
     
        for (int r = 0; r < modeloC.getRowCount(); r++) 
            for (int c = 0; c < modeloC.getColumnCount(); c++) 
                jTableC.setValueAt(C.getValor(r,c),r,c);
    }
