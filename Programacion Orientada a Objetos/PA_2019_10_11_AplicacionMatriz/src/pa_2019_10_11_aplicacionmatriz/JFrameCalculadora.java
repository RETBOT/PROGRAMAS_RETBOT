    private void jButAleatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButAleatorioActionPerformed
        // TODO add your handling code here:
        Random azar = new Random();
        
        for (int r = 0; r < jTableA.getRowCount() ; r++) {
            for (int c = 0; c < jTableA.getColumnCount() ; c++) {
                 jTableA.setValueAt(azar.nextInt(100), r, c);
            }
        }
        
        for (int r = 0; r < jTableB.getRowCount() ; r++) { // recorrer renglones 
            for (int c = 0; c < jTableB.getColumnCount() ; c++) { // recorrer columnas 
                 jTableB.setValueAt(azar.nextInt(100), r, c);
            }
        }
        
    }//GEN-LAST:event_jButAleatorioActionPerformed

