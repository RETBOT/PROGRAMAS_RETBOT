    private void jButCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcularActionPerformed
        // Declaraion de varianles 
        String alumno;
        int asistencias, tareas, proyectos, examen;
       
       // Entrada de datos 
       try{
       alumno = jTextAlumno.getText();
       asistencias = Integer.parseInt(jTextAsistencia.getText());
       tareas = Integer.parseInt(jTextTareas.getText());
       proyectos = Integer.parseInt(jTextProyectos.getText());
       examen = Integer.parseInt(jTextExamen.getText());
       }
       catch (Exception ex){
      JOptionPane.showMessageDialog(rootPane, "Datos de entrada incorrectos... ");
       return;
      }
      
      //proceso de datos
        double asi =  asistencias * 0.10;
        double tar =  tareas     * 0.12;
        double proy = proyectos   * 0.18;
        double exam = examen     * 0.60;  
        
        double calif = asi+tar+proy+exam;
        
        
        // salida 
        jLabNombre.setText("Alumno: "+alumno);
        jLabCalificacion.setText("Calificacion: "+calif+"");
        
                
        jLabNombre.setBackground(jButCalcular.getBackground());
        jLabCalificacion.setBackground(jButCalcular.getBackground());
    }//GEN-LAST:event_jButCalcularActionPerformed

