    public double volumenCono(double r, double h)  
    {
//       double v;
//       v = Math.PI * Math.pow(r,2)*h/3;
//       return v;
    return Math.PI*Math.pow (r,2)+h/3;
    }
    
    public double generatriz(double r, double h)
    {
    return Math.sqrt(Math.pow(r,2) + Math.pow(h,2));
    }
    
    public double areaCono(double r, double h)
    {
    return Math.PI*r*(r+generatriz(r,h));
    }
    
    
