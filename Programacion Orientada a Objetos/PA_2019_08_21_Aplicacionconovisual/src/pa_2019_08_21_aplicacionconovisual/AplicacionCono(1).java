        area       = Math.PI * radio * (radio + generatriz);

        //salida
        jLabArea.setText(area+"");
        jLabVolumen.setText(volumen+"");
        
        jLabArea.setBackground(jButCalcular.getBackground());
        jLabVolumen.setBackground(jButCalcular.getBackground());


       
