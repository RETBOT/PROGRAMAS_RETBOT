    private void jButCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcularActionPerformed
        // TODO add your handling code here:
        Distancia dInicial = new Distancia();
        Distancia dFinal = new Distancia();
        Distancia dRecorrida;
        Distancia dTotal = new Distancia();
        
        // Se llena la informaci√≥n 
        for(int r=0; r<modelo.getRowCount(); r++ ){
        dInicial.setMilla(Integer.parseInt(modelo.getValueAt(r, 1)+""));
        dInicial.setBraza(Integer.parseInt(modelo.getValueAt(r, 2)+""));
        dInicial.setYarda(Integer.parseInt(modelo.getValueAt(r, 3)+""));
        dInicial.setPie(Integer.parseInt(modelo.getValueAt(r, 4)+""));
        dInicial.setPulgada(Integer.parseInt(modelo.getValueAt(r, 5)+""));
          
        dFinal.setMilla(Integer.parseInt(modelo.getValueAt(r, 6)+""));
        dFinal.setBraza(Integer.parseInt(modelo.getValueAt(r, 7)+""));
        dFinal.setYarda(Integer.parseInt(modelo.getValueAt(r, 8)+""));
        dFinal.setPie(Integer.parseInt(modelo.getValueAt(r, 9)+""));
        dFinal.setPulgada(Integer.parseInt(modelo.getValueAt(r, 10)+""));        
        
        // Se calcula la distancia recorrida 
        dRecorrida = dFinal.resta(dInicial);
        
        modelo.setValueAt(dRecorrida, r, 11);
        
        
        //se suman las distancias 
        dTotal = dTotal.suma(dRecorrida);
        }
        // Se muestran las distancia total en el jLab 
        jLabTotal.setText(dTotal+"");
                
        
    }//GEN-LAST:event_jButCalcularActionPerformed

